// Задайте одномерный массив из 10 целых чисел от 1 до 100. 
// Найдите количество элементов массива, значения которых лежат в отрезке [20,90].


using System;

class Program
{
    static void Main()
    {
        // Задаем одномерный массив из 10 целых чисел от 1 до 100
        int[] array = { 15, 30, 45, 60, 75, 90, 10, 25, 40, 5 };

        // Находим количество элементов массива, значения которых лежат в отрезке [20,90]
        int count = 0;
        foreach (int element in array)
        {
            if (element >= 20 && element <= 90)
            {
                count++;
            }
        }

        // Выводим результат
        Console.WriteLine($"Количество элементов в отрезке [20,90]: {count}");
    }
}